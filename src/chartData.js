export const parseTimeZone = (timestamp, timeZone) => {
  const t = moment.unix(timestamp).tz(timeZone)
  return t.format('YYYY-MM-DDTHH:mm:ss.SSS')
}

export const convertToLocalTime = (alerts) => {
  alerts.forEach(r => r.occurredAt = parseTimeZone(r.occurredAt, r.timeZone))

  return alerts
}